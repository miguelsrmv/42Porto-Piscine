int	ft_atoi(char *str)
{
	int	sign;
	int	nb;

	sign = 0;
	nb = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32
		|| *str == '-' || *str == '+')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10;
		nb = nb + *str - 48;
		str++;
	}
	return (nb * sign);
}
